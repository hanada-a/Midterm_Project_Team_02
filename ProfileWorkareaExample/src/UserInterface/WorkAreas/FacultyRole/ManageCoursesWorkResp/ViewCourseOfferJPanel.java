    public ViewCourseOfferJPanel(JPanel p, CourseOffer co, Business b) {
        initComponents();
        
        this.CardSequencePanel = p;
        this.courseOffer = co;
        this.business = b;
        
        // display both Course and CourseOffer fields
        txtCourseId.setText(courseOffer.getCourse().getCourseId());
        txtCourseName.setText(courseOffer.getCourse().getCourseName());
        cmbCredits.setSelectedItem(String.valueOf(courseOffer.getCourse().getCredits()));
        cmbIsCore.setSelectedItem(courseOffer.getCourse().getIsCore() ? "Yes" : "No");
        txtSemester.setText(courseOffer.getSemester());
        txtSeats.setText(String.valueOf(courseOffer.getSeatCount()));
        
        // populate faculty box and select current faculty
        cmbFacultyAssignment.removeAllItems();
        for (FacultyProfile fp : business.getFacultyDirectory().getFacultyList()) {
            cmbFacultyAssignment.addItem(fp.getPerson().getName());
        }
        if (courseOffer.getFacultyassignment() != null) {
            cmbFacultyAssignment.setSelectedItem(courseOffer.getFacultyassignment().getFaculty().getPerson().getName());
        }
        
        refreshAssignmentsTable();
        
    }
    
    
    private void refreshAssignmentsTable() {
        DefaultTableModel model = (DefaultTableModel) tblAssignments.getModel();
        model.setRowCount(0);

        for (Assignment a : courseOffer.getAssignmentList()) {
            Object[] row = new Object[1];
            row[0] = a.getAssignmentName();
            model.addRow(row);
        }
    }
    
    
    private void backAction() {
        CardSequencePanel.remove(this);
        Component[] componentArray = CardSequencePanel.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageCoursesJPanel parent = (ManageCoursesJPanel) component;
        parent.refreshTable();
        CardLayout layout = (CardLayout) CardSequencePanel.getLayout();
        layout.previous(CardSequencePanel);
    }
    

